#!/usr/bin/env bash
set -euo pipefail

repo_root="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
out_dir="${repo_root}/inst/extdata"
tmp_dir="$(mktemp -d)"
trap 'rm -rf "${tmp_dir}"' EXIT

require_cmd() {
  if ! command -v "$1" >/dev/null 2>&1; then
    echo "Missing required command: $1" >&2
    exit 1
  fi
}

require_cmd samtools

{
  printf ">11\n"
  head -c 50000 /dev/zero | tr '\0' 'A' | fold -w 100
} > "${out_dir}/fixture_ref.fa"

samtools faidx "${out_dir}/fixture_ref.fa"

cat > "${tmp_dir}/fixture_paired.sam" <<'EOF'
@HD	VN:1.6	SO:coordinate
@SQ	SN:11	LN:50000
pair1	99	11	1001	60	36M	=	1101	136	AAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAA	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
pairdup	99	11	1001	60	36M	=	1101	136	CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
pair1	147	11	1101	60	36M	=	1001	-136	TTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTT	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
pairdup	147	11	1101	60	36M	=	1001	-136	GGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGG	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
EOF

cat > "${tmp_dir}/fixture_single.sam" <<'EOF'
@HD	VN:1.6	SO:coordinate
@SQ	SN:11	LN:50000
single1	0	11	2001	60	36M	*	0	0	AAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAA	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
singledup	0	11	2001	60	36M	*	0	0	CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
EOF

cat > "${tmp_dir}/fixture_mixed.sam" <<'EOF'
@HD	VN:1.6	SO:coordinate
@SQ	SN:11	LN:50000
pair1	99	11	1001	60	36M	=	1101	136	AAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAA	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
pairdup	99	11	1001	60	36M	=	1101	136	CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
pair1	147	11	1101	60	36M	=	1001	-136	TTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTT	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
pairdup	147	11	1101	60	36M	=	1001	-136	GGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGG	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
flagdup	1123	11	6001	60	36M	=	6101	136	AAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAA	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
flagdup	1171	11	6101	60	36M	=	6001	-136	TTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTT	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
single1	0	11	12001	60	36M	*	0	0	CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
singledup	0	11	12001	60	36M	*	0	0	GGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGGG	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
pairbad	65	11	16001	60	36M	=	16101	136	AAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAAA	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
pairbad	129	11	16101	60	36M	=	16001	-136	TTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTTT	FFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFFF
EOF

make_bam() {
  local sam="$1"
  local bam="$2"
  samtools view -b -o "${bam}" "${sam}"
  samtools index "${bam}"
}

make_bam "${tmp_dir}/fixture_paired.sam" "${out_dir}/fixture_paired.bam"
make_bam "${tmp_dir}/fixture_single.sam" "${out_dir}/fixture_single.bam"
make_bam "${tmp_dir}/fixture_mixed.sam" "${out_dir}/fixture_mixed.bam"

samtools view -C -T "${out_dir}/fixture_ref.fa" \
  -o "${out_dir}/fixture_mixed.cram" \
  "${tmp_dir}/fixture_mixed.sam"
samtools index "${out_dir}/fixture_mixed.cram"
